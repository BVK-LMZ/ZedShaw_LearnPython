# Functions can return values

def add(arg_a: int, arg_b: int):
    return arg_a + arg_b

def subtract(arg_a: int, arg_b: int):
    return arg_a - arg_b

def Multiply(arg_a: int, arg_b: int):
    return arg_a * arg_b

def Divide(arg_a: int, arg_b: int):
    return arg_a / arg_b

def Modulus(arg_a: int, arg_b: int):
    return arg_a % arg_b

def exponentiate(arg_a: int, arg_b: int):
    return arg_a ** arg_b


